let monTableau = ['Davy' /** 0 **/, 'Marine' /** 1 **/, 'Bob' /** 2 **/]

console.log(monTableau) // ['Davy', 'Marine', 'Bob']

// Il est possible d'accéder à des éléments individuels du tableau de la façon suivante:
console.log(monTableau[0]) // 'Davy'

// Il est également possible de modifier les éléments d'un array de la manière suivante
monTableau[1] = 'Richard'
console.log(monTableau) // ['Davy', 'Richard', 'Bob']

